#!/usr/bin/env python3
"""Module that provides a string concatenation function."""


def concat(str1: str, str2: str) -> str:
    """ Concatenate two strings and return the concatenated result.

    Args:
    str1 (str): The first string.
    str2 (str): The second string.

    Return:
    str: The result of concatenating str1 and str2.
    """
    return str1 + str2
